#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn catalog_frames_are_valid_v3_socket_payloads() {
        for preset in catalog() {
            assert!(!preset.frames.is_empty(), "{}", preset.id);
            assert!(preset.frames.len() <= 100, "{}", preset.id);
            for frame in preset.frames {
                assert_eq!(frame.len(), 16, "{} {frame}", preset.id);
                assert!(
                    frame.bytes().all(|b| b.is_ascii_hexdigit()),
                    "{} {frame}",
                    preset.id
                );
            }
            assert!(preset.pulse_command(Channel::A).len() <= MAX_MESSAGE_LENGTH);
            assert!(preset.pulse_command(Channel::B).len() <= MAX_MESSAGE_LENGTH);
        }
    }

    #[test]
    fn next_waveform_cycles_catalog_order() {
        assert_eq!(next_waveform_id("breath"), Some("tide"));
        assert_eq!(next_waveform_id("tease_2"), Some("breath"));
        assert_eq!(next_waveform_id("missing"), None);
    }
}
