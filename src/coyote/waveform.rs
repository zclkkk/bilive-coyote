pub const DEFAULT_WAVEFORM_ID: &str = "breath";
pub const NEXT_WAVEFORM: &str = "next";

#[derive(Debug, Clone, Copy)]
pub struct WaveformPreset {
    pub id: &'static str,
    pub name: &'static str,
    pub frames: &'static [&'static str],
}

#[derive(Debug, Clone, Serialize)]
#[serde(rename_all = "camelCase")]
pub struct WaveformInfo {
    pub id: &'static str,
    pub name: &'static str,
    pub frame_count: usize,
    pub duration_ms: u64,
}

impl WaveformPreset {
    pub fn info(&self) -> WaveformInfo {
        WaveformInfo {
            id: self.id,
            name: self.name,
            frame_count: self.frames.len(),
            duration_ms: self.duration().as_millis() as u64,
        }
    }

    pub fn pulse_command(&self, channel: Channel) -> String {
        let prefix = match channel {
            Channel::A => "pulse-A:",
            Channel::B => "pulse-B:",
        };
        let frames = serde_json::to_string(self.frames).expect("waveform frames serialize");
        format!("{prefix}{frames}")
    }

    pub fn repeat_interval(&self) -> Duration {
        self.duration()
            .checked_sub(Duration::from_millis(100))
            .unwrap_or_else(|| Duration::from_millis(100))
    }

    fn duration(&self) -> Duration {
        Duration::from_millis(self.frames.len() as u64 * 100)
    }
}

pub fn catalog() -> &'static [WaveformPreset] {
    PRESETS
}

pub fn list_waveforms() -> Vec<WaveformInfo> {
    catalog().iter().map(WaveformPreset::info).collect()
}

pub fn find_waveform(id: &str) -> Option<&'static WaveformPreset> {
    catalog().iter().find(|preset| preset.id == id)
}

pub fn is_waveform_id(id: &str) -> bool {
    find_waveform(id).is_some()
}

pub fn next_waveform_id(current: &str) -> Option<&'static str> {
    let catalog = catalog();
    let index = catalog.iter().position(|preset| preset.id == current)?;
    Some(catalog[(index + 1) % catalog.len()].id)
}

